    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nid = jTextField7.getText();
        String fname = jTextField1.getText();
        String lname = jTextField3.getText();
        String contactNo = jTextField2.getText();
        String email = jTextField6.getText();
        String userName = jTextField8.getText();
        String password = jPasswordField1.getText();
        String rePassword = jPasswordField2.getText();
        String line1 = jTextField4.getText();
        String line2 = jTextField5.getText();
        String city = jComboBox1.getSelectedItem().toString();
        String workingType = jComboBox2.getSelectedItem().toString();

        int gender = 1;

        if (jRadioButton1.isSelected()) {
            gender = 1;
        } else if (jRadioButton2.isSelected()) {
            gender = 2;
        }

        if (workingType.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select Working Type", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (nid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Idinty card number", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter First Name", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Last Name", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Email", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("(((([a-z]|[0-9])*)[.][a-z](([a-z]|[0-9])*))|(([a-z]|[0-9])*))[@][a-z|0-9][a-z|0-9]*[.][a-z]([a-z]*)").matcher(email).matches()) {
            JOptionPane.showMessageDialog(this, "Enter Valid Email", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (userName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter User name", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Password", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (rePassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Re Password", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (!rePassword.equals(password)) {
            JOptionPane.showMessageDialog(this, "Invalid Re Password", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (contactNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Contact Number", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[1245678][0-9]{7}").matcher(contactNo).matches()) {
            JOptionPane.showMessageDialog(this, "Enter Valid Contact Number", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (line1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Address line 1", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (line2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Address line 2", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select city")) {
            JOptionPane.showMessageDialog(this, "Select City", "Worning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs1 = MySql.search("SELECT * FROM `working_people_rs` WHERE `working_people_rs`.`nic`='" + nid + "' OR `working_people_rs`.`username`='" + userName + "'");

                if (rs1.next()) {
                    JOptionPane.showMessageDialog(this, "alredy exsite employe registard or Username alredy exsite", "Worning", JOptionPane.WARNING_MESSAGE);

                } else {

                    ResultSet rs2 = MySql.search("SELECT * FROM `city` WHERE `name`='" + city + "'");
                    rs2.next();
                    String city_id = rs2.getString("id");

                    MySql.iud("INSERT INTO `address`(`line_1`,`line_2`,`city_id`)VALUES('" + line1 + "','" + line2 + "','" + city_id + "')");

                    ResultSet rs3 = MySql.search("SELECT LAST_INSERT_ID();");
                    rs3.next();
                    String address_id = rs3.getString(1);

                    ResultSet rs4 = MySql.search("SELECT * FROM `working_type` WHERE `name`='" + workingType + "'");
                    rs4.next();
                    String workingTypeId = rs4.getString("id");

                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    String dNow = sdf.format(new Date());

                    MySql.iud("INSERT INTO `working_people_rs`(`nic`,`fname`,`lname`,`contact_no`,`email`,`address_id`,`working_type_id`,`gender_id`,`join_date`,`username`,`password`,`working_status_id`)"
                            + "VALUES( '" + nid + "','" + fname + "','" + lname + "','" + contactNo + "','" + email + "','" + address_id + "','" + workingTypeId + "'," + gender + ",'" + dNow + "','" + userName + "','" + password + "','1')");

                    JOptionPane.showMessageDialog(this, "Employe account created", "Sucsess", JOptionPane.INFORMATION_MESSAGE);

                    jTextField7.setText("");
                    jTextField1.setText("");
                    jTextField3.setText("");
                    jTextField2.setText("");
                    jTextField6.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField8.setText("");
                    jPasswordField1.setText("");
                    jPasswordField2.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jComboBox2.setSelectedIndex(0);
                    jRadioButton1.setSelected(true);

                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

