    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String anb = jTextField1.getText();
        String brand = jComboBox1.getSelectedItem().toString();

        if (brand.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select Brand", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (anb.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter model name", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (brand.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select Brand", "Worning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet rs = MySql.search("SELECT * FROM `vehicle_brand` WHERE `name`='" + brand + "'");

                if (rs.next()) {

                    String brandId = rs.getString("id");
                    MySql.iud("INSERT INTO `vehicle_model`(`name`,`vehicle_brand_id`)VALUES ('" + anb + "','" + brandId + "')");

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

            JOptionPane.showMessageDialog(this, "New model added", "Sucsess", JOptionPane.INFORMATION_MESSAGE);

            av.lord_model();
            jTextField1.setText("");
            lord_model();

        }
    }//GEN-LAST:event_jButton1ActionPerformed

